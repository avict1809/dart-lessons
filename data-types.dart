/*
  Data types
  Numbers, Strings, Boolean, Lists, Maps, Dynamic
*/

void main(){
  //Strings
  String firstName = "Avict";
  print("String: $firstName");

  //Integers
  int myNum = 45;
  print("Integer: $myNum");

  //Doubles
  double myDouble = 45.5;
  print("Double: $myDouble");

  //Boolean
  bool myBool = true;
  print("Boolean: $myBool");

  //Dynamic
  dynamic fullName = "John Doe";
  print("Dynamic: $fullName");
}